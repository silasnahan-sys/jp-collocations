/** A collocation span extracted from a running sentence. */
export interface ExtractedCollocation {
  /** Exact text found in the sentence. */
  span: string;
  startIndex: number;
  endIndex: number;
  headword: string;
  collocate: string;
  /** Pattern code e.g. "N+みたいなところから+AdjPred" */
  pattern: string;
  /** Japanese label e.g. "比喩距離述語型" */
  patternLabel: string;
  /** Broad category e.g. "比喩照合型" */
  patternCategory: string;
  headwordPOS: PartOfSpeech;
  collocatePOS: PartOfSpeech;
  confidence: number;
}

interface PatternSpec {
  regex: RegExp;
  pattern: string;
  patternLabel: string;
  patternCategory: string;
  extractHead: (m: RegExpExecArray) => string;
  extractCollocate: (m: RegExpExecArray) => string;
  headPOS: PartOfSpeech;
  colPOS: PartOfSpeech;
  confidence: number;
}

// ---------------------------------------------------------------------------
// Pattern builder — generates thousands of collocation pattern specs
// ---------------------------------------------------------------------------

function sp(
  regex: RegExp,
  pattern: string,
  patternLabel: string,
  patternCategory: string,
  extractHead: (m: RegExpExecArray) => string,
  extractCollocate: (m: RegExpExecArray) => string,
  headPOS: PartOfSpeech,
  colPOS: PartOfSpeech,
  confidence: number,
): PatternSpec {
  return { regex, pattern, patternLabel, patternCategory, extractHead, extractCollocate, headPOS: headPOS, colPOS, confidence };
}

